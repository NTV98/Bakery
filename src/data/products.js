// Enhanced pagination utility function with category filtering
export const paginateProducts = (page = 1, perPage = 12, categoryFilter = 'all') => {
  // Filter products by category if needed
  const filteredProducts = categoryFilter === 'all' 
    ? products 
    : products.filter(product => product.category === categoryFilter);
  
  const startIndex = (page - 1) * perPage;
  const endIndex = startIndex + perPage;
  
  return {
    currentPage: page,
    totalPages: Math.ceil(filteredProducts.length / perPage),
    totalItems: filteredProducts.length,
    hasNextPage: endIndex < filteredProducts.length,
    hasPrevPage: page > 1,
    products: filteredProducts.slice(startIndex, endIndex)
  };
};

// Get specific page of products with optional category filter
export const getProductsPage = (page = 1, categoryFilter = 'all') => {
  return paginateProducts(page, 12, categoryFilter);
};


